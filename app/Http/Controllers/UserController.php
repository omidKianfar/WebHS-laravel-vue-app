     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user=User::find($id);
        if($user->type == ''){
            $user->update (["type"=> 'admin']);
            return redirect()->route('users.index')->with('success', 'User has been admin');
        }
        else{
            $user->update(["type"=> '']);
            return redirect()->back()->with('warning', 'User hasnt been admin');
        }
    }
    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        file::delete("../public/dist/img/user-img/".$user->image);
        $user->delete();
        return redirect()->back()->with('completed', 'User has been deleted');
    }

